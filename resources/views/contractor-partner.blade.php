            if (isChatOpen && !chatbotWindow.contains(e.target) && !chatbotToggle.contains(e.target)) {
                // Allow clicks inside chatbot window
                if (!chatbotWindow.contains(e.target)) {
                    closeChatbot();
                }
            }
        });
